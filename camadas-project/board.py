def print_table(table: list) -> None:
    print("     1   2   3")
    print(f"A    {table[0][0]} | {table[0][1]} | {table[0][2]}")
    print("    ---------")
    print(f"B    {table[1][0]} | {table[1][1]} | {table[1][2]}")
    print("    ---------")
    print(f"C    {table[2][0]} | {table[2][1]} | {table[2][2]}")
